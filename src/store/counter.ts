import { create } from 'zustand';

interface CounterState {
  count: number;
  increment: () => void;
  decrement: () => void;
  reset: () => void;
}

// Este store es un ejemplo de infraestructura (implementación de almacenamiento de estado)
// Puedes mover la lógica de dominio a /domain y exponer solo la interfaz aquí si lo deseas.

export const useCounterStore = create<CounterState>((set) => ({
  count: 0,
  increment: () => set((state) => ({ count: state.count + 1 })),
  decrement: () => set((state) => ({ count: state.count - 1 })),
  reset: () => set({ count: 0 }),
}));
